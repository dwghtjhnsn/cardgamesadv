/**
 * class Hand can represent a hand of cards. It isthe same as class Deck 
 * removing the fill and shuffle methods.
 * 
 * @author Josh Bush 
 * @version December 18, 2015
 */
public class Hand extends Deck 
{
    /**
     * Hand Constructor
     *
     */
    public Hand() 
    {
        super();
    }

    /**
     * Method fill
     * This method was in Deck and is removed for Hand
     */
    protected void fill()
    {
    }

    /**
     * Method shuffle
     * This method was in Deck and is removed for Hand
     */
    public void shuffle()
    {
    }
}
   